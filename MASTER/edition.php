     <p><em class="sous_titre_edition">Présentation</em><br />
 </p>
<p>Afin d'adapter son offre aux nouveaux besoins ergonomiques lié à l'usage du réseau, le collectif IPX oriente sa production du côté de la publication papier et numérique au sein du module ÉDITION. <br /><br />Pour cela il prend en compte l'histoire et l'émergence des nouveaux supports de lecture - ordinateurs, smartphones, tablettes, liseuses – entre lesquels les contenus circulent, afin de proposer des créations éditoriales originales et sur mesure.</p>
     <p><br />
     </p>
    
    
     <p><em class="sous_titre_edition">Composition de la recherche</em><br />
 </p>
<p>Ce module est mis en place à partir d'un savoir (et savoir-faire) évolutif, dynamique et partagé. Il se situe au croisement de différentes problématiques de recherche contemporaines - entre accessibilité de l'ouvrage, enjeux économiques modernes et nouveaux modes de création et de distribution – à partir desquels les membres du collectif tendent à développer une chaîne de production éditoriale inédite.</p>
 <p><br />
     </p>
    
  
     <p><em class="sous_titre_edition">Objectifs</em><br />
 </p>
<p>EDITION réunit des services d'expertise tel qu' un accompagnement personnalisé pour la réalisation de livres papier et électroniques ainsi qu'un accès à « l'observatoire » des pratiques éditoriales, une base de ressources libre, gratuite et accessible à tous.</p>
<p>
Les pratiques personnelles d'artistes et de graphistes des membres du collectif, viennent également alimenter le contenu des différentes prestations proposé au sein ce module. De la création d'un concept de collection, au dévellopement d'une charte graphique originale, à un travail de mise en page ou ou de typographie précis. etc.</p>
<p>
Afin de toujours privilégier le contact et d'être au plus prés des besoins de ses clients, la plateforme IPX génère des devis comprenant des temps d'échanges et de discussions que le collectif considère comme indispensable dans l'élaboration de chaque projet. Ainsi IPX prend en charge la conception d'objets éditoriaux aux contenus hétérogènes, qu'il s'agisse d'un livre d'artiste, d'un manuel de ressources pédagogique, d'un support de communication ou d'un livre de recettes. </p>
 <p><br />
     </p>
    
 <p><em class="sous_titre_edition">Perspectives</em><br />
 </p>
<p>La plateforme Internet eXchange Point est à envisager comme un relais, un nœud dans le réseau de distribution globale des informations. En faisant un pas de côté vis-à-vis de modèles éditoriaux traditionnels, en questionnant le statut de l'auteur, tout en lançant sa propre ligne éditoriale le collectif IPX tendra à interroger de façon générale le champ de diffusion des savoirs.
