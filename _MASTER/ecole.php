<p align="center"><i>Internet eXchange Point</i></p>
     <p align="center"><br />
     </p>
     <p><em class="sous_titre_ecole">Présentation</em><br />
 </p>
<p>Afin  de répondre à la demande de formation aux outils numériques  aujourd’hui très présente dans la société civile, mais aussi  plus largement afin d’accompagner les individus vers une  autonomisation de leur environnement technologique, le collectif IPX  oriente sa recherche du côté de la pédagogie au sein du module <em>É</em><em>COLE</em>.</p>
     <p>En  proposant un enseignement à la fois original et ancrée au sein de  la création artistique contemporaine, nous proposons différents  formats d’apprentissage où l’accent est mis sur la créativité  et l’émancipation des individus face aux nouveaux médias.<br />
     </p>
<p>&nbsp;</p>
<p><em class="sous_titre_ecole">Composition  de la recherche</em><br />
</p>
     <p>La  création de ce module trouve son origine dans les valeurs de la  culture open-source, un mouvement en faveur de la diffusion, du  partage et de la valorisation du savoir. Plus  largement ancré dans le champ des Humanités  Numériques<em>,  É</em><em>COLE</em> se caractérise également par des méthodes et des pratiques liées  à l'utilisation des outils numériques - en ligne et hors ligne -  ainsi que par la volonté de prendre en compte les nouveaux contenus  numériques, au même titre que des objets d'étude plus  traditionnels.  Parce que les supports numériques et leur accès au réseau  bouleversent les conditions de production et de diffusion du savoir,  le collectif IPX choisit d’orienter son enseignement autour des  problématiques sous-jacente au <em>savoir-lire-écrire</em> numérique.<br />
   </p>
<p>&nbsp;</p>
<p><em class="sous_titre_ecole">Objectif</em><br />
     </p>
<p>IPX  propose différents formats d’apprentissages afin d’adapter son  offre aux besoins et aux emplois du temps de chacun.  Ainsi,   les membres du collectif organisent différents&nbsp;temps d’échange  - sous forme de cours particuliers, d’ateliers et de workshops - où  l’accent est mis sur l’expérimentation de formes, d’outils et  de langages liés aux nouveaux médias. Ce  module  met directement à profit les instants de découverte, de partage et  de vie dans un processus pédagogique tourné vers la mutualisation  des connaissances.</p>
     <p><br />
     </p>
     <p><em>É</em><em>COLE </em>a  pour objectif de mettre en place un accompagnement humain permettant  à tous  d’apprendre à lire, traiter et organiser les images et les  informations. Si celles-ci saturent aujourd’hui l’espace - des  murs de nos villes jusqu’aux pages du web – il semble devenir  nécessaire d’accompagner l’individu dans cet environnement. Dans  ce sens, les différentes formations proposées par IPX tendent  toutes à mettre le doigt  sur l'architecture et la matérialité du réseau mais aussi sur la  question du flux.</p>
     <p><br />
     </p>
     <p>En  s'appuyant sur des expérimentations concrètes et en  sensibilisant les élèves à différents types d’attention – à  soi, à l’autre, à l’écran, à l’environnement –  l’équipe  d’IPX vise à mettre en place les bases d’une école où le flux  de la libre circulation des données ne sera non plus subit et  consommé&nbsp;; mais sera détourné à des fins créatifs.</p>
     <p><br />
     </p>
    
     <p><em class="sous_titre_ecole">Perspectives</em><br />
     </p>
<p>Devenir  «&nbsp;école&nbsp;» est l’un des objectifs principaux d’<em>Internet  eXchange Point.</em> Nous considérons que le modèle de la classe doit  s'appuyer sur un  nouveau système de transmission où le rôle de l'enseignant n'est  plus uniquement celui qui détient le savoir, mais où celui-ci  assure la position d'un médiateur entre l'élève et le réseau des  connaissances accessible sur le web. Pour cela, nous développons les  bases d'un nouveau cadre  où le bien-être, le développement, l’expression et la production  individuelle sera possible.</p>
