       différents projets. La forme de l'outil prend la forme d'un CMS ou d'un centre de ressource accessible et libre de
       droit accessible sur notre site internet. Elle accumule et organise un ensemble d'échantillons d'expérimentation,
       fragment de code à ré-agencer, de discussion, d'exemples concrets, tutoriels, etc L'objectif est l'ouverture <br />
