     <p><em class="sous_titre_edition">Présentation</em><br />
     </p>
     <p>Cette partie tend à proposer une formation ou une sensibilisation à de nouvelles branches de métiers liés
aux usages et aux pratiques du numérique. Nous tenterons de cartographier les différents éléments constitutifs de
la chaîne de production (Capture de l'information, traitement des données et restitutions des données). Nous mettrons
en place des stratégies qui piloteront nos sessions de travail en groupe afin d'optimiser les compétences de chacun,
de favoriser la mixité sociale et professionnelle et de mettre en place un travail situé dans une évolution créative et
durable au sein d'une équipe, d'un groupe ou du collectivité.<br />
       <br />
       Cette ligne de recherche s'inscrit dans le courant des Software Studies, des Humanité digital et de l'archéologie des
       médias.</p>
     <p><br />
       À travers le développement d'une plate-forme appelé IP-F, nous tenterons de mettre en place une structure qui convienne à nos besoins et qui pourra dans un second temps s'adapter à l'évolution des designs et des<br />
       nouveautés..<br />
     </p>
     <p><em class="sous_titre_edition">Composition de la recherche</em><br />
   </p>
     <p>Nous œuvrons à la mise en place d'un framework simplifié, adapté, personnalisé à la structure et à l'identité
d'une entreprise. Il s'agit d'orienter et de trouver un modèle propre à l'identité de chaque projet et d'adapter les
outils en fonction du projet et de ses caractéristique. Il s'agit d'offrir à l'utilisateur la possibilité d'augmenter sa prise
de décision, d'agrémenter le nombre de possibles et d'enrichir ses choix. De plus, notre postulat est de sensibiliser
le public aux facettes éthiques et juridiques du Web, pour l'amener à se positionner, à faire des choix en harmonie
avec ses besoins quotidien et son développement personnel. Nous souhaitons mettre en place un espace qui favorise
et tisse du lien entre différents corps de métiers. Ce projet s'adapte à tout type de client, professionnel, amateur,
jeune travailleur, retraité, cadre, chef d'entreprise ou encore demandeur d'emploi.</p>
     <p>1 – Service d'accompagnement, de conseil et d'expertise à la conception de systèmes d'informations.</p>
     <p>2 – Service de fabrication adapté et sur mesure de sites (ou application) Web.<br />
       Développement de logiciels, nouveaux outils, d'autres systèmes de fonctionnalité pour aller vers des pratiques et
       des usages éthique, open-source et le plus écologique possible<br />
       L'accompagnement d'une idée au service d'un projet professionnel.</p>
     <p>3- Mise en place de méthode collective par une expérimentation et une création méthodologique de travail propre
       aux membres d'IPX. Ce processus aura pour but de construire différentes trames de travail ou la mise en place de
       framework (par exemple sous forme de tutoriaux) utile pour les ateliers. Une grande part sera donnée à la sensibilisation aux langages de programmation afin de proposer aux utilisateurs des système de navigation appropriés. C'est
       un support sous forme de partition qui porte d'avantage l'objet de la discussion plutôt que le sujet de la discussion.</p>
     <p>4- Création d'un centre de Ressources</p>
     <p>5- Situer et appréhender à tout les niveaux le fonctionnement des réseaux et la diffusion de l'information : comment fonctionne le référencement, le page Ranking, des sites etc…</p>
     <p>Par le prisme de ce dispositif, nous tendons à réduire, voir supprimer, le nombre grandissant d'interprétation, par conséquent de filtre, permettant non seulement d'accéder à la libre circulation des information, mais aussi
       d'y participer. Nous tenterons de réduire le langage exprimé au sentiment intime les plus simples et authentique<br />
       <br />
 </p>
