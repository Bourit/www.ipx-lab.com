<p align="center"><i>Internet eXchange Point</i></p>
     <p align="center"><br />
       Présentation<br />
       Cette partie tend à proposer une formation ou une sensibilisation à de nouvelles branches de métiers liés
       aux usages et aux pratiques du numérique. Nous tenterons de cartographier les différents éléments constitutifs de
       la chaîne de production (Capture de l'information, traitement des données et restitutions des données). Nous mettrons
       en place des stratégies qui piloteront nos sessions de travail en groupe afin d'optimiser les compétences de chacun,
       de favoriser la mixité sociale et professionnelle et de mettre en place un travail situé dans une évolution créative et
      durable au sein d'une équipe, d'un groupe ou du collectivité. </p>
     <p align="center"> Cette ligne de recherche s'inscrit dans le courant des Software Studies, des Humanité digital et de l'archéologie des<br />
       médias.<br />
       À travers le développement d'une plate-forme appelé IP-F, nous tenterons de mettre en place une struc-<br />
       ture qui convienne à nos besoins et qui pourra dans un second temps s'adapter à l'évolution des designs et des<br />
       nouveautés.<br />
       Composition de la recherche<br />
       Nous œuvrons à la mise en place d'un framework simplifié, adapté, personnalisé à la structure et à l'identité<br />
       d'une entreprise. Il s'agit d'orienter et de trouver un modèle propre à l'identité de chaque projet et d'adapter les<br />
       outils en fonction du projet et de ses caractéristique. Il s'agit d'offrir à l'utilisateur la possibilité d'augmenter sa prise<br />
       de décision, d'agrémenter le nombre de possibles et d'enrichir ses choix. De plus, notre postulat est de sensibiliser<br />
       le public aux facettes éthiques et juridiques du Web, pour l'amener à se positionner, à faire des choix en harmonie<br />
       avec ses besoins quotidien et son développement personnel. Nous souhaitons mettre en place un espace qui favorise<br />
       et tisse du lien entre différents corps de métiers. Ce projet s'adapte à tout type de client, professionnel, amateur,<br />
       jeune travailleur, retraité, cadre, chef d'entreprise ou encore demandeur d'emploi.<br />
       Objectif<br />
       1 – Service d'accompagnement, de conseil et d'expertise à la conception de systèmes d'informations<br />
       2 – Service de fabrication adapté et sur mesure de sites (ou application) Web.<br />
       Développement de logiciels, nouveaux outils, d'autres systèmes de fonctionnalité pour aller vers des pratiques et<br />
       des usages éthique, open-source et le plus écologique possible<br />
       L'accompagnement d'une idée au service d'un projet professionnel.<br />
       3- Mise en place de méthode collective par une expérimentation et une création méthodologique de travail propre<br />
       aux membres d'IPX. Ce processus aura pour but de construire différentes trames de travail ou la mise en place de<br />
       framework (par exemple sous forme de tutoriaux) utile pour les ateliers. Une grande part sera donnée à la sensibili-<br />
       sation aux langages de programmation afin de proposer aux utilisateurs des système de navigation appropriés. C'est<br />
       un support sous forme de partition qui porte d'avantage l'objet de la discussion plutôt que le sujet de la discussion.<br />
       4- Création d'un centre de Ressources<br />
       5- Situer et appréhender à tout les niveaux le fonctionnement des réseaux et la diffusion de l'information : com-<br />
       ment fonctionne le référencement, le page Ranking, des sites etc…<br />
       Par le prisme de ce dispositif, nous tendons à réduire, voir supprimer, le nombre grandissant d'interpréta-<br />
       tion, par conséquent de filtre, permettant non seulement d'accéder à la libre circulation des information, mais aussi<br />
       d'y participer. Nous tenterons de réduire le langage exprimé au sentiment intime les plus simples et authentique.<br />
       Perspectives<br />
       a) Conservation :<br />
       La conservation, l'hébergement relatif au lieu de stockage. Question relative à l'archéologie des médias et la ques-<br />
       tion de la conservation : rapport à l'hébergement des données / au serveur / où sont stockées les données ? Le for-<br />
       mat des base de données / La traçabilité, du filtrage, de la fiabilité, de la protection des données.<br />
       b) Architecture de l'information :<br />
       À l'organisation des documents de travail. L'arborescence des fichiers / architecture [Rapport entre architecture et<br />
       circulation du flux des données] : ce que l'utilisateur ne voit pas = statique. L'entre-deux / code, langage, flux =<br />
       dynamique<br />
       c) Interface :<br />
       Mise en page, règle typographique, colorimétrie, responsive design, UX, Navigation, norme et standard, syntaxe,<br />
       Html et Css<br />
       Ouverture<br />
       La base de ressource associé au module IP:S a pour vocation d'être utilisé comme une boîte à outil au service des<br />
       différents projets. La forme de l'outil prend la forme d'un CMS ou d'un centre de ressource accessible et libre de<br />
       droit accessible sur notre site internet. Elle accumule et organise un ensemble d'échantillons d'expérimentation,<br />
       fragment de code à ré-agencer, de discussion, d'exemples concrets, tutoriels, etc L'objectif est l'ouverture d'un lieu<br />
       atelier qui permettra de réaliser cette aventure à une échelle humaine et de radical.<br /><br />
